package com.revature;

public class Launcher {

    //This is the main method - the starting point for a Java application
    public static void main(String[] args){

        //This is a print statement
        //It allows us to "print" a message to the console (the box that pops up on app runtime)
        System.out.println("Hello World!");

        System.out.println("We are learning very fundamental Java");

        System.out.println("We can type 'sout' to quickly generate a print statement");


        /*
         System = This system. The console.
         out = output, we're defining some output
         println = prints the message on its own line
         */

    }

}
